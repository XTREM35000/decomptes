import fs from 'fs'
import path from 'path'
import crypto from 'crypto'

const envPath = path.resolve(process.cwd(), '.env')

// üõ°Ô∏è G√©n√©rer une cl√© secr√®te s√©curis√©e
const JWT_SECRET = crypto.randomBytes(32).toString('hex')

try {
  let envContent = ''
  if (fs.existsSync(envPath)) {
    envContent = fs.readFileSync(envPath, 'utf8')
  }

  // Mettre √† jour ou ajouter JWT_SECRET
  if (envContent.includes('JWT_SECRET=')) {
    envContent = envContent.replace(/JWT_SECRET=.*/g, `JWT_SECRET=${JWT_SECRET}`)
  } else {
    envContent += `\nJWT_SECRET=${JWT_SECRET}`
  }

  fs.writeFileSync(envPath, envContent)
  console.log('‚úÖ Nouveau JWT_SECRET g√©n√©r√© avec succ√®s et ajout√© √† .env')
} catch (error) {
  console.error('‚ùå Erreur lors de la g√©n√©ration du JWT_SECRET :', error)
  process.exit(1)
}
